#include "libft.h"

int	ft_toupper(int c)
{
	if (c < EOF && c > -UCHAR_MAX)
	{
		c = -c;
	}
	if (c >= 'a' && c <= 'z')
	{
		c -= ('a' - 'A');
	}
	return (c);
}
